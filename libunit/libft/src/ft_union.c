#include "libft.h"

void	ft_union(char *str)
{
	int	tab[256];
	int	a;

	a = 0;
	while (str[a])
	{
		if (tab[(int)str[a]] == 0)
		{
			tab[(int)str[a]] = 1;
		}
		a++;
	}
}
